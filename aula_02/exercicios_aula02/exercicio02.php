<?php
    // Define um número
    $numero = 10;

    // Verifica se é maior ou igual a 1
    if ($numero >= 1) {
        // Se sm, o número é positivo
        echo "O número $numero é positivo.";
    } else if ($numero == 0) {
        // Se for igual a 0, o número é zero
        echo "O número $numero é zero.";
    } else {
        // Se é menor do que zero, o número é negativo
        echo "O número $numero é negativo.";
    }
?>